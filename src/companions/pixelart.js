// ═══ PIXEL ART DRAW ═══════════════════════════════════════════════
function drawPixelArt(canvas, artKey, size, glowColor, tier) {
  tier = tier||1;
  const art = PA[artKey];
  if(!art){canvas.width=size;canvas.height=size;return;}
  const GRID=8, ps=Math.floor(size/GRID);
  canvas.width=size; canvas.height=size;
  const ctx=canvas.getContext('2d');
  ctx.clearRect(0,0,size,size);
  ctx.imageSmoothingEnabled=false;
  // glow background (rareté)
  if(glowColor){
    ctx.shadowColor=glowColor; ctx.shadowBlur=8;
    ctx.fillStyle=glowColor+'22';
    ctx.fillRect(0,0,size,size);
    ctx.shadowBlur=0;
  }
  const pal=['#00000000',...art.pal]; // idx 0=transparent
  art.px.forEach((row,r)=>{
    for(let c=0;c<8;c++){
      const ci=parseInt(row[c]);
      if(ci===0)continue;
      ctx.fillStyle=pal[ci];
      ctx.fillRect(c*ps,r*ps,ps,ps);
    }
  });
  // pixel border highlight (top-left lighter)
  art.px.forEach((row,r)=>{
    for(let c=0;c<8;c++){
      const ci=parseInt(row[c]);
      if(ci===0)continue;
      if(r===0||(parseInt(row[c-1]||'0')===0)){
        ctx.fillStyle='rgba(255,255,255,0.25)';
        ctx.fillRect(c*ps,r*ps,1,ps);
      }
      if(r===0){ctx.fillStyle='rgba(255,255,255,0.2)';ctx.fillRect(c*ps,r*ps,ps,1);}
    }
  });

  // ═══ ÉVOLUTION VISUELLE PAR TIER ═══
  // Même nom, même forme de base, mais l'aura/l'éclat s'intensifie visuellement de T1 à T5.
  if(tier>=2){
    const TIER_VISUAL = [null,null,
      {ring:'#e8e8e8', alpha:0.35, sparkles:1}, // T2 — liseré blanc discret
      {ring:'#7fd4ff', alpha:0.5,  sparkles:2}, // T3 — liseré bleu clair
      {ring:'#c98fff', alpha:0.65, sparkles:3}, // T4 — liseré violet
      {ring:'#ffd700', alpha:0.85, sparkles:5}, // T5 — liseré doré + sparkles intenses
    ];
    const tv = TIER_VISUAL[Math.min(tier,5)];
    if(tv){
      // Liseré de contour qui s'intensifie
      ctx.strokeStyle = tv.ring;
      ctx.globalAlpha = tv.alpha;
      ctx.lineWidth = 1 + tier*0.4;
      ctx.strokeRect(1,1,size-2,size-2);
      ctx.globalAlpha = 1;
      // Étincelles positionnées de façon déterministe (mêmes positions à chaque rendu du même pet)
      const seed = artKey.split('').reduce((a,c)=>a+c.charCodeAt(0),0) + tier*17;
      for(let i=0;i<tv.sparkles;i++){
        const sx = ((seed*(i+3)*7) % (size-6))+2;
        const sy = ((seed*(i+5)*13) % (size-6))+2;
        const sp = Math.max(1.5, size*0.035);
        ctx.fillStyle = tv.ring;
        ctx.globalAlpha = 0.9;
        ctx.fillRect(sx, sy, sp, sp);
        ctx.globalAlpha = 1;
      }
    }
  }
}
